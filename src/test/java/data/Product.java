package data;

public class Product {
    private String name;
    private String price;
    private String availability;
    private String code;


    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getAvailability() {
        return availability;
    }

    public String getCode() {
        return code;
    }

}
